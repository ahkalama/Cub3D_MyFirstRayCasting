#include <stdio.h>

void	color_av(int c)
{
	char	*t;
	char	*t1;
	char	*t2;
	int		a;
	int		f;

	a = 0;
	f = 1;
	while (1)
	{
		t = "				███████████████████████████████████████";
		t1 = "█████████████████L̳O̳A̳D̳I̳N̳G̳... %";
		t2 = "████████████████████████████████████████████████████████";
		printf("\033[48;5;%dm\033[38;5;%dm%s%s%d%s\033[0m", a, a, t, t1,
			c / 1000, t2);
		printf("\r");
		a = (a + 1);
		f++;
		c++;
		if (f == 100002)
		{
			printf("\n");
			break ;
		}
	}
}
